// Writing Java Program to convert kilometers to miles 
public class KilometersToMiles{
      public static void main(String[] args){
            //creating a double variable to store distance in kilometers which is in decimal
             double distanceInKilometers = 10.8;
            //creating a double variable to calculate distance in kilometers which may come in decimal
             double distanceInMiles = 10.8 * 0.621371;

             // Displaying result
             System.out.println("The distance " + distanceInKilometers + " km in miles is " + distanceInMiles);
       }
}